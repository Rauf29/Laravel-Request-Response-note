<?php
// create a new table
/*
php artisan make::migration create_tableName;
exaple: php artisan make:migration create_users_table;
 */

// add a new column
/*
php artisan make:migration add_columnName_to_tableName;
exaple: php artisan make:migration add_name_to_users;
 */

// rename or update a column
/*
php artisan make:migration update_columnName_to_tableName;
exaple: php artisan make:migration update_name_to_users;
 */

// remove a column
/*
php artisan make:migration remove_columnName_from_tableName;
exaple: php artisan make:migration remove_name_from_users;
 */

// drop a table
/*
php artisan make:migration drop_tableName;
exaple: php artisan make:migration drop_users;
 */